#include <bits/stdc++.h>

void processTestCase() { std::cout << std::endl; }

int main() {
  std::ios_base::sync_with_stdio(false);
  std::cin.tie(nullptr);
  std::cout.tie(nullptr);
  int tests;
  std::cin >> tests;
  for (int testCase = 1; testCase <= tests; testCase += 1) {
    std::cout << "Case #" << testCase << ": ";
    processTestCase();
  }
  return 0;
}